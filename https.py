def get_random_agent() -> str:
    """
    Randoms random user-agent for requests
    """
    return str(random.choice(user_agents))


def create_request(
    mode: str,
    host: str,
    url: str,
    proxies: str = proxies.get_random_proxy(),
    headers: dict = {"User-Agent": str(get_random_agent())},
    body=None,
    timeout: int = 7,
) -> http.client.HTTPResponse:
    """
    Creates request based on desired mode, much faster than the standard Request library. Used for making fast pased requests
    """
    try:
        if proxies:
            conn = http.client.HTTPSConnection(proxies, timeout=timeout)
            conn.set_tunnel(
                host, port=443, headers=headers
            )  # HTTPS is 443, python.http.client.http i think
        else:  # Direct connection to host instead
            conn = http.client.HTTPSConnection(host)
        conn.request(
            mode,
            url,
            headers=headers,
            body=body,
        )
        return conn.getresponse()
    except Exception as exc:
        # Pass exception, usually a simple error that can be ignored **usually**
        # print(exc)
        pass
